package org.ooad.HITHAM.model;

public class SongListModel {
 private String songlist_name;
 private String songlist_url;
 private String songlist_pic_url;
 private String songlist_song_color;
 private int songlist_playlist;
public String getSonglist_name() {
	return songlist_name;
}
public void setSonglist_name(String songlist_name) {
	this.songlist_name = songlist_name;
}
public String getSonglist_url() {
	return songlist_url;
}
public void setSonglist_url(String songlist_url) {
	this.songlist_url = songlist_url;
}
public String getSonglist_pic_url() {
	return songlist_pic_url;
}
public void setSonglist_pic_url(String songlist_pic_url) {
	this.songlist_pic_url = songlist_pic_url;
}
public String getSonglist_song_color() {
	return songlist_song_color;
}
public void setSonglist_song_color(String songlist_song_color) {
	this.songlist_song_color = songlist_song_color;
}
public int getSonglist_playlist() {
	return songlist_playlist;
}
public void setSonglist_playlist(int songlist_playlist) {
	this.songlist_playlist = songlist_playlist;
}
	
	 
 
}
